var r = +prompt("Введите радиус цилиндра мм.", " ");
var h = +prompt("Введите высоту цилиндра мм.", " ");
var p = 3.141592653589793;
var v = p * (r * r) * h;
var s = 2 * p * r * (h + r); 
document.write("Обьем цилиндра = " + v + " мм." + "<br/>");
document.write("Площадь основания цилиндра = " + s + " мм.кв.")